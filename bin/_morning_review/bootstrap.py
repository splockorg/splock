"""`--internal-bootstrap-day` handler (implplan §H.impl.9 seam 1).

Called by `bin/_retry_loop/halt_handoff.py::_ensure_morning_review_file`
when the per-plan daily file does not yet exist. Mkdir-p's the
`docs/plans/<slug>/morning-review/` dir and writes the date-stamped
header atomically.

Idempotent: no-op if the daily file already exists.
"""

from __future__ import annotations

import os
import pathlib
from typing import Optional

from . import queue_file


def run(
    *,
    repo_root: pathlib.Path,
    slug: str,
    date_iso: str,
    chain_id: Optional[str] = None,
) -> int:
    """Bootstrap the per-plan daily file. Returns process exit code.

    Per §H.impl.9: writes header
      `# Morning Review — <date>`
      `\n`
      `Generated by <emitter>. Operator triage required.`
      `\n`
      `---`

    `<emitter>` resolves to `chain driver <chain_id>` when chain_id is
    set (halt-handoff path), else `bin/morning-review --internal-bootstrap-day`
    (cold-start).
    """
    plan_dir = repo_root / "docs" / "plans" / slug
    plan_dir.mkdir(parents=True, exist_ok=True)
    # Fall back to SPLOCK_CHAIN_ID env if caller didn't pass one (the
    # halt-handoff caller normally subprocess-runs us with env intact).
    if chain_id is None:
        env_cid = os.environ.get("SPLOCK_CHAIN_ID")
        if env_cid:
            chain_id = env_cid
    queue_file.bootstrap_day(plan_dir, date_iso, chain_id=chain_id)
    return 0
